using UnityEngine;
using TMPro;

public class ChangeIDMenuButton : MenuButton
{
    [field: SerializeField]
    private TextMeshProUGUI CurrentIDText;
    public override void OnClick()
    {
        PlayerID.playerID = (PlayerID.playerID + 1) % 2;
        CurrentIDText.text = "ID : " + PlayerID.playerID;
    }
}
